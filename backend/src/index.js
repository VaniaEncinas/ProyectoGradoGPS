const express = require("express");
const cors = require("cors");
require("dotenv").config();

const authRoutes = require("./routes/authRoutes");
const ninosRoutes = require("./routes/ninosRoutes");
const ubicacionesRoutes = require("./routes/ubicacionesRoutes");
const pulseraRoutes = require("./routes/pulseraRoutes"); // ðŸ‘ˆ Importamos rutas de pulsera

const app = express();
app.use(cors());
app.use(express.json());

// Middleware para debug
app.use((req, res, next) => {
  console.log("ðŸ“¥ PeticiÃ³n recibida:", req.method, req.url);
  console.log("ðŸ“¦ Body:", req.body);
  next();
});

// Rutas
app.use("/api/auth", authRoutes);
app.use("/api/ninos", ninosRoutes);
app.use("/api/ubicaciones", ubicacionesRoutes);
app.use("/api/pulseras", pulseraRoutes); // ðŸ‘ˆ Registramos rutas pulsera

// Middleware para rutas no encontradas
app.use((req, res) => {
  res.status(404).json({ message: "Ruta no encontrada" });
});

// Puerto del servidor Node
const PORT = process.env.PORT || 4000;
app.listen(PORT, () => {
  console.log(`ðŸš€ Servidor backend corriendo en http://localhost:${PORT}`);
});
